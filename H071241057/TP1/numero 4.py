print("\nOPERASI PERUBAHAN CELCIUS\n")
celcius = float(input("suhu dalam celcius:"))
print("suhu :", celcius, "derajat")

reamur = 4/5*celcius
print(f"hasil konversi dari suhu {celcius} derajat celcius ke reamur adalah {reamur}R")

kelvin = 273 + celcius
print(f"hasil konversi dari suhu {celcius} derajat celcius ke reamur adalah {kelvin}K")

fahreinheit = (9/5)*celcius + 32
print(f"hasil konversi dari suhu {celcius} derajat celcius ke reamur adalah {fahreinheit}F")
