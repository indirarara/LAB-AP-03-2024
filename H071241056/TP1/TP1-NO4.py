suhu = int(input('masukkan suhu dalam celcius = '))

kelvin = suhu + 273
reamur = (4*suhu)/5
fahreinheit = (9*suhu)/5+32

print('hasil konversi dari suhu 50 derajat celcius ke kelvin adalah', kelvin,'K')
print('hasil konversi dari suhu 50 derajat celcius ke reamur adalah', int(reamur),'R')
print('hasil konversi dari suhu 50 derajat celcius ke fahreinheit adalah', int(fahreinheit),'F')

